let currentAdminLang = localStorage.getItem('admin_lang') || 'zh';

function t(key) {
  const dict = ADMIN_I18N[currentAdminLang] || ADMIN_I18N['zh'];
  return dict[key] || ADMIN_I18N['zh'][key] || key;
}

function setAdminLang(lang) {
  currentAdminLang = lang;
  localStorage.setItem('admin_lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
}
